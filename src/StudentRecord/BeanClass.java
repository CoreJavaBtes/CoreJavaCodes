package StudentRecord;

public class BeanClass {
	String c_code,c_name;
	

	public BeanClass(String c_code, String c_name) {
		this.c_code = c_code;
		this.c_name = c_name;
	}
	
	

	public String getC_code() {
		return c_code;
	}

	public String getC_name() {
		return c_name;
	}

}
