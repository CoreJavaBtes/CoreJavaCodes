package StudentRecord;

public class Student_bean {
	String s_id,s_name;

	public Student_bean(String s_id, String s_name) {
		super();
		this.s_id = s_id;
		this.s_name = s_name;
	}

	public String getS_id() {
		return s_id;
	}

	public String getS_name() {
		return s_name;
	}
}
